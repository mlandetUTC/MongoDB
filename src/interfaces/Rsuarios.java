        // en este envento realizaremos la validacion de los campos para solo numeros.
        char teclas;
        teclas = evt.getKeyChar();
        if(!Character.isDigit(teclas) && teclas!=KeyEvent.VK_BACK_SPACE)
        {
            evt.consume();
            getToolkit().beep();//emite sonido
        }

        ///validamos limite de caracteres para este campo
        int numeroingreso=10;
        if(txtcd.getText().length()>= numeroingreso)
        {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Limite Superado ","Mensaje",JOptionPane.INFORMATION_MESSAGE);
        }

